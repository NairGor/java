package com.example.samost13;

public class tableNumbers {
    public double KI;
    public double YI;

    public tableNumbers(double KI, double YI) {
        this.KI = KI;
        this.YI = YI;

    }

    public double getKI() {
        return KI;
    }

    public void setKI(double KI) {
        this.KI = KI;
    }

    public double getYI() {
        return YI;
    }

    public void setYI(double YI) {
        this.YI = YI;
    }
}
